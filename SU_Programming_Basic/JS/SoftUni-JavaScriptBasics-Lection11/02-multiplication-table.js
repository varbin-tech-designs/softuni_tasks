function multiplicationCycle() {
    for (multiplier1 = 1; multiplier1 <= 10; multiplier1++){
        for (multiplier2 = 1; multiplier2 <= 10; multiplier2++) {
            console.log(`${multiplier1} * ${multiplier2} = ${multiplier1 * multiplier2}`);
            
        }
    }
}

